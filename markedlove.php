        <h2>Background</h2>
        <p><i>Marked Love</i> started as a proof of concept for a two-player puzzle platformer.   Players control two opposing hitmen after the same mark, and they must cooperate in order to reach their goals.</p>
        <p>It was created as my second project for Fooad Khosmood's experimental <i>Interactive Entertainment Engineering</i> course at Cal Poly. The goal here was to explore how to convey character and personality in games.  In Marked Love's case, I attempted to use the gameplay as characterization, giving each player unique abilities and designing a level around them.</p>

